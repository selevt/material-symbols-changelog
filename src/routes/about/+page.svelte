<p>
    This is to provide a changelog for the Material Symbols icons, as they appear on <a href='https://fonts.google.com/icons'>Google Fonts</a>.
</p>

<p>
    This site is updated once a day, so there might be a delay after changes have been released.
    The shown changes are only for the outlined variant, changes for the filled variants are currently not considered.
</p>

<p>
    The difference calculation is done on the SVGs. Changes with minimal differences are filtered out.
</p>

<p>
    This project is open source and hosted on GitHub at <a href='https://github.com/selevt/material-symbols-changelog'>selevt/material-symbols-changelog</a>.
</p>